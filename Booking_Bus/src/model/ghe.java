package model;

public class ghe {
	private String MaGhe;
	private String MaXe;
	public ghe(String maGhe, String maXe) {
		super();
		MaGhe = maGhe;
		MaXe = maXe;
	}
	/**
	 * @return the maGhe
	 */
	public String getMaGhe() {
		return MaGhe;
	}
	/**
	 * @param maGhe the maGhe to set
	 */
	public void setMaGhe(String maGhe) {
		MaGhe = maGhe;
	}
	/**
	 * @return the maXe
	 */
	public String getMaXe() {
		return MaXe;
	}
	/**
	 * @param maXe the maXe to set
	 */
	public void setMaXe(String maXe) {
		MaXe = maXe;
	};
	

}
