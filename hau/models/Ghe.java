package models;

public class Ghe {
	private String maGhe;
	private String maXe;
	public String getMaGhe() {
		return maGhe;
	}
	public void setMaGhe(String maGhe) {
		this.maGhe = maGhe;
	}
	public String getMaXe() {
		return maXe;
	}
	public void setMaXe(String maXe) {
		this.maXe = maXe;
	}
	public Ghe(String maGhe, String maXe) {
		super();
		this.maGhe = maGhe;
		this.maXe = maXe;
	}
	

}
